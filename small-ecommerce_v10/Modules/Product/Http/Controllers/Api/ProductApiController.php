    /**
     * Display a listing of the resource.
     * @return Renderable
     */
    public function index(Request $request)
    {
        return Call::TryCatchResponseJson(function() use ($request) {
            $tag = $request->get('tag');
            if(!isset($tag))
            {
                $products = $this->productService->all();
            }
            $user = Auth::user();
            $products = $this->productService->getProductByTag($tag,$user);
            return ResponseJson::success(data:$products);
        });
    }

